
/* Inferring types */
let hello = "Hello World";
console.log(hello);

hello = 5;
console.log(hello);

/* Explicit types */
let hello2: string = "Hello World";
console.log(hello2);

hello2 = 5;
console.log(hello2);

let hello3: any = "Hello World";
console.log(hello3);
hello3 = 7;
